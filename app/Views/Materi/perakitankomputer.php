                <p>
                Sistem komputer terdiri atas perangkat keras (hardware), perangkat lunak (software), dan pengguna (brainware). Ketiga perangkat ini merupakan kesatuan yang tak terpisahkan dalam sistem komputer. Perangkat keras (hardware) sebagian bagian dari system komputer dibentuk oleh komponen-komponen tertentu, yakni komponen masukan (input device), komponen alat pemroses (processing device), komponen alat keluaran (output device), dan komponen alat simpanan luar (storage). Sistem operasi (Operating Sistem = OS) merupakan perangkat lunak (software) untuk mengendalikan dan mengkoordinasikan aktifitas operasi komputer. OS berfungsi sebagai pemimpin dalam sebuah organisasi sistem komputer berikut komponen-komponen komputer.</p>

                
