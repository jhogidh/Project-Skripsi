                    <li>Digital komputer yaitu komputer yang dibuat untuk mengolah data yang bersifat kuantitatif, proses kerjanya bersifat arithmetic dan logic.</li>
                    <li>Hybrid komputer merupakan gabungan antara Analog Komputer dengan Digital Komputer. Data diterima secara analog dan diproses secara digital.</li>
